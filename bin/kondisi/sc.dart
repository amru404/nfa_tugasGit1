// switch case

void main(){
    var nilai = "F";
    
    switch(nilai){
        case 'A' : print('anda lulus dengan nilai a'); break;
        case 'B' : print('anda lulus dengan nilai B'); break;
        case 'C' : print('anda lulus dengan nilai C'); break;
        case 'D' : print('anda lulus dengan nilai D'); break;
        default : print('ga lulus');
    }
}